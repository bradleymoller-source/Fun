// Group monsters by CR for easy access
export const MONSTERS_BY_CR: Record<string, Partial<Monster>[]> = {};
MONSTER_PRESETS.forEach(monster => {
  const cr = monster.challengeRating || '0';
  if (!MONSTERS_BY_CR[cr]) {
    MONSTERS_BY_CR[cr] = [];
  }
  MONSTERS_BY_CR[cr].push(monster);
});

// Get all CRs available
export const AVAILABLE_CRS = Object.keys(MONSTERS_BY_CR).sort((a, b) => {
  const parseValue = (cr: string) => {
    if (cr.includes('/')) {
      const [num, denom] = cr.split('/').map(Number);
      return num / denom;
    }
    return parseFloat(cr);
  };
  return parseValue(a) - parseValue(b);
});
